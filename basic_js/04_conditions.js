// if else

let a = 1;

if (a == 1){
    console.log(true);
}else{
    console.log(false);
}


// if / else if / else

if (a == 1){
    console.log("a == 1");
}else if(a == 2){
    console.log("a == 2");
}else{
    console.log("not");
}


// Switch case

let day = 1;

switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
        console.log("Saturday");
        break;
    case 7:
        console.log("Sunday");
        break;
    default:
        console.log("Not Found");
        break;
}